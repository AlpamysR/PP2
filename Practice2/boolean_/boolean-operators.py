# ==	Equal	x == y	
# !=	Not equal	x != y	
# >	    Greater than	x > y	
# <	    Less than	x < y	
# >=	Greater than or equal to	x >= y	
# <=	Less than or equal to	x <= y

x = 5
y = 3
print(x == y) #False
print(x != y) #True
print(x > y)  #True
print(x < y)  #False
print(x >= y) #True
print(x <= y) #False

#Complicated comparison operators
x = 5
print(1 < x < 10) #True
print(1 < x and x < 10) #True
